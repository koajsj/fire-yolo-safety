#!/usr/bin/env bash
# ============================================================
# 消防安全风险分析与预警系统 - macOS / Linux 一键启动脚本
# 同时启动后端(FastAPI) 与 前端(Vite)
# ============================================================
set -e

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "============================================================"
echo "  消防安全风险分析与预警系统 启动中..."
echo "============================================================"

# ---------- 启动后端 ----------
echo "[1/2] 启动后端服务 (http://127.0.0.1:8000) ..."
cd "$ROOT/backend"
python3 -m uvicorn main:app --host 127.0.0.1 --port 8000 &
BACKEND_PID=$!

# 等待后端初始化
sleep 3

# ---------- 启动前端 ----------
echo "[2/2] 启动前端服务 (http://127.0.0.1:5173) ..."
cd "$ROOT/frontend"
npm run dev &
FRONTEND_PID=$!

echo ""
echo "============================================================"
echo "  启动完成！"
echo "  前端访问地址: http://127.0.0.1:5173"
echo "  后端接口文档: http://127.0.0.1:8000/docs"
echo "  无需登录，打开前端地址即可直接使用"
echo "============================================================"
echo ""
echo "按 Ctrl+C 停止全部服务。"

# 捕获退出信号，关闭子进程
trap "echo '正在停止服务...'; kill $BACKEND_PID $FRONTEND_PID 2>/dev/null; exit 0" INT TERM

wait
